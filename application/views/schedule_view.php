	<p style="text-align: center; text-align: center;"><strong><span style="color: #ff6600;">УСЛУГИ ---- Ультразвуковая диагностика</span></strong>
	</p>
	<p><strong>Подготовка к проведению УЗИ и УЗДГ</strong>
	</p>
	<p><strong>Ультразвуковое исследование (УЗИ)</strong> – это неинвазивный, то есть без проколов и разрезов, метод диагностики значительного числа заболеваний сердечно-сосудистой системы (ССС), пищеварительной системы, мочеполовой системы, позволяющий кроме того получить нужные сведения в акушерстве и гинекологии, онкологии, офтальмологии, травматологии и т.д.
	</p>
	<p>Для того чтобы исследование прошло успешно, перед его проведением необходимо придерживаться некоторых правил.
	</p>
	<p><strong>1. УЗИ органов брюшной полости, включая печень, селезенку, поджелудочную железу и желчный пузырь</strong>
	</p>
	<p>Исследование проводится натощак, от последнего приема пищи должно пройти не менее 8 часов.
	</p>
	<p>За 3 дня до исследования необходимо отказаться от употребления продуктов, усиливающих газообразование в кишечнике: сырых овощей, цельного молока, черного хлеба, бобовых, газированных напитков, пирожных и тортов.
	</p>
	<p>За несколько дней до исследования следует принимать ферментные препараты, которые порекомендует врач. Также не забудьте сообщить врачу о принимаемых Вами лекарствах и уточните схему их приема до проведения УЗИ.
	</p>
	<p>Курильщикам стоит воздержаться от курения за 2 часа до исследования.
	</p>
	<p><strong>2. УЗИ почек</strong>
	</p>
	<p>Для проведения этого исследования необходимо соблюдать обычный водный и пищевой режим. Ограничения требуются только в случае приема мочегонных препаратов, что необходимо согласовать с лечащим врачом.
	</p>
	<p><strong>3. УЗИ мочевого пузыря, предстательной железы, органов малого таза </strong>
	</p>
	<p>За час до исследования пациенту необходимо выпить один литр негазированной жидкости.
	</p>
	<p><strong>6. УЗИ беременных</strong>
	</p>
	<p>Проведение<strong> УЗИ у беременных </strong>зависит от срока и определяется врачом.<strong> </strong>
	</p>
	<p><strong>Трансвагинальное исследование </strong>при малом сроке беременности не требует особой подготовки,<strong> трансабдоминальное </strong>проводится при наполненном мочевом пузыре – за час до исследования пациентке необходимо выпить 500 мл (пол-литра) негазированной жидкости, если врач не предложил другое.
	</p>
	<p><strong>7. УЗИ полового члена и мошонки, головного мозга, плевральных полостей, щитовидной железы, магистральных сосудов головы и шеи, сосудов верхних и нижних конечностей, мягких тканей, тазобедренных суставов, лимфатических узлов и сердца </strong>проводится без специальной подготовки.
	</p>
	<p><strong>8. УЗИ молочных желез</strong> женщин осуществляется предпочтительней на седьмой-двенадцатый день менструального цикла, если иное не указано лечащим врачом.
	</p>
