	<p style="text-align: center; text-align: center;"><strong><span style="color: #ff6600;">УСЛУГИ ---- ФИЗИОТЕРАПИЯ</span></strong>
	</p>
	<p><span style="color: #535353;">Физиотерапия, как метод оздоровления зародилась в глубине веков. Природные лечебные факторы активно использовали в своей практике врачи Древней Греции и Рима. Об этом же свидетельствуют труды гениального ученого и врача Ибн-Сины (Авиценны), в которых обобщен опыт врачей Византии по использованию солнца, воздушных ванн и воды для оздоровления организма и лечения болезней.</span>
	</p>
	<p><span style="color: #535353;"></span>
	</p>
	<p><span style="color: #535353;">В наш XXI век, век автоматизации, урбанизации и загрязнения окружающей среды, перенапряжения нервной системы, гиподинамии все большая роль отводится физическим факторам, как природным, так и преформированным (искусственным). Сегодня врачи располагают десятками тысяч фармакологических препаратов, большинство из которых может иметь выраженные побочные действия, прежде всего аллергические. </span>
	</p>
	<p><span style="color: #535353;">Природные (климат, минеральные воды для наружного и внутреннего применения, лечебные грязи) и искусственные (импульсные токи, магнитные и электромагнитные поля, лазерное излучение, хромо- и светотерапия, фотодинамическая терапия, ультразвук, криотерапия, озонотерапия, галотерапия и др.) физические факторы не только не вызывают аллергических реакций, но обычно оказывают противоаллергическое действие.</span>
	</p>
	<p><span style="color: #535353;">Если же в физиотерапии и используются лекарственные препараты (электрофорез, аэрозоли), то их дозировка в десятки раз меньше, чем при приеме внутрь или парентеральном введении, так как введение препарата идет непосредственно на очаг заболевания, где лекарственное вещество накапливается, создавая так называемое «лекарственное депо», а не разносится по всему организму. Рациональное сочетание медикаментозной терапии и физических факторов в значительной мере увеличивают эффективность лечения и медицинской реабилитации больных.</span>
	</p>
	<p><span style="color: #535353;">Задача – восстановление и повышение уровня здоровья – успешно решают физиотерапевтические методы лечения, основанные на применении различных природных факторов. Один из ведущих физиотерапевтов современности, доктор медицинских наук, профессор В.С. Улащик писал: «</span><strong><span style="color: #535353;">…сохранение здоровья человека возможно при помощи естественного образа жизни, а восстановление нарушенного недугом здоровья и профилактика болезней – при помощи естественных способов лечения</span></strong><span style="color: #535353;">». </span>
	</p>
	<p><span style="color: #535353;"> Преимущества физиотерапии:</span>
	</p>
	<p><span style="color: #656464;">1. Универсальность. </span>
	</p>
	<p><span style="color: #656464;">2. Физиологичность воздействий. </span>
	</p>
	<p><span style="color: #656464;">3. Тренирующий эффект. </span>
	</p>
	<p><span style="color: #656464;">4. Отсутствие токсичности и аллергизации. </span>
	</p>
	<p><span style="color: #656464;">5. Возможность снижения побочных эффектов лекарственных средств при их сочетании с физиотерапией. </span>
	</p>
	<p><span style="color: #656464;">6. Возможность длительного последействия. </span>
	</p>
	<p><span style="color: #656464;">7. Широкий диапазон методик применения физических факторов. </span>
	</p>
	<p><span style="color: #656464;">8. Доступность использования преформированных природных факторов в лечебно-профилактических и санаторно-курортных учреждениях. </span>
	</p>
